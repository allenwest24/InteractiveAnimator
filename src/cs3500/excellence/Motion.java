package cs3500.excellence;

public class Motion {

  final int tick;
  final int x;
  final int y;
  final int height;
  final int width;
  final Color color;

  Motion(int tick, int x, int y, int height, int width, Color color) {
    this.tick = tick;
    this.x = x;
    this.y = y;
    this.height = height;
    this.width = width;
    this.color = color;
  }


}
